package model;

public class OverSummaryVO {
	
	private Integer fixtureId;
	private Integer over;
	private Integer run;
	private Integer wkt;
	private Integer batter1Id;
	private Integer batter2Id;
	private Integer bowlerId;
	private String bowlerName;
	private String batter1Name;
	private String batter2Name;
	
	public Integer getFixtureId() {
        return fixtureId;
    }

    public void setFixtureId(Integer fixtureId) {
        this.fixtureId = fixtureId;
    }

    public Integer getOver() {
        return over;
    }

    public void setOver(Integer over) {
        this.over = over;
    }

    public Integer getRun() {
        return run;
    }

    public void setRun(Integer run) {
        this.run = run;
    }

    public Integer getWkt() {
        return wkt;
    }

    public void setWkt(Integer wkt) {
        this.wkt = wkt;
    }

    public Integer getBatter1Id() {
        return batter1Id;
    }

    public void setBatter1Id(Integer batter1Id) {
        this.batter1Id = batter1Id;
    }

    public Integer getBatter2Id() {
        return batter2Id;
    }

    public void setBatter2Id(Integer batter2Id) {
        this.batter2Id = batter2Id;
    }

    public Integer getBowler() {
        return bowlerId;
    }

    public void setBowler(Integer bowler) {
        this.bowlerId = bowler;
    }

    public String getBowlerName() {
        return bowlerName;
    }

    public void setBowlerName(String bowler) {
        this.bowlerName = bowler;
    }
    
    public String getBatter1Name() {
        return batter1Name;
    }

    public void setBatter1Name(String batter1Name) {
        this.batter1Name = batter1Name;
    }

    public String getBatter2Name() {
        return batter2Name;
    }

    public void setBatter2Name(String batter2Name) {
        this.batter2Name = batter2Name;
    }
	
    
}
